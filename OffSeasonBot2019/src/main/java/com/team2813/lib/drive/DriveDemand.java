package com.team2813.lib.drive;

public class DriveDemand {
    private final double left;
    private final double right;

    public DriveDemand(double left, double right) {
        this.left = left;
        this.right = right;
    }

    public double getLeft() {
        return left;
    }

    public double getRight() {
        return right;
    }
}
